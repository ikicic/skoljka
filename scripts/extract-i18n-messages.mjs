import { readdirSync, readFileSync, writeFileSync } from "fs";
import { join, relative } from "path";
import ts from "typescript";

const ROOT = "static/ts";
const OUTPUT = "static/ts/i18n-messages.ts";
const EXCLUDED = new Set(["static/ts/i18n.ts", OUTPUT]);

function* sourceFiles(dir) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) {
      yield* sourceFiles(path);
    } else if (/\.(ts|tsx)$/.test(entry.name) && !EXCLUDED.has(path)) {
      yield path;
    }
  }
}

function literalText(node) {
  if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) {
    return node.text;
  }
  return null;
}

const gettextMessages = new Map();
const ngettextMessages = new Map();

for (const path of [...sourceFiles(ROOT)].sort()) {
  const source = readFileSync(path, "utf8");
  const ast = ts.createSourceFile(path, source, ts.ScriptTarget.Latest, true, ts.ScriptKind.TSX);

  function visit(node) {
    if (ts.isCallExpression(node) && ts.isIdentifier(node.expression)) {
      const name = node.expression.text;
      if (name === "gettext" && node.arguments.length >= 1) {
        const message = literalText(node.arguments[0]);
        if (message !== null && !gettextMessages.has(message)) {
          gettextMessages.set(message, path);
        }
      } else if (name === "ngettext" && node.arguments.length >= 2) {
        const singular = literalText(node.arguments[0]);
        const plural = literalText(node.arguments[1]);
        if (singular !== null && plural !== null) {
          const key = `${singular}\u0000${plural}`;
          if (!ngettextMessages.has(key)) {
            ngettextMessages.set(key, { singular, plural, path });
          }
        }
      }
    }
    ts.forEachChild(node, visit);
  }

  visit(ast);
}

const lines = [
  "import { gettext, ngettext } from \"./i18n\";",
  "",
  "// Generated by scripts/extract-i18n-messages.mjs.",
  "// Keep TSX strings visible to Django's JavaScript message extractor.",
  "void [",
];

for (const [message, path] of gettextMessages) {
  lines.push(`  gettext(${JSON.stringify(message)}), // ${relative(".", path)}`);
}
for (const { singular, plural, path } of ngettextMessages.values()) {
  lines.push(
    `  ngettext(${JSON.stringify(singular)}, ${JSON.stringify(plural)}, 2), // ${relative(".", path)}`,
  );
}

lines.push("];", "");
writeFileSync(OUTPUT, lines.join("\n"));
